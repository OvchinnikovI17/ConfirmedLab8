class AnswerToUser {

    /**
     * writeToConsole method. Print text using print.
     *
     * @param whatToPrint String?
     */
    fun writeToConsole(whatToPrint: String?){
        print(whatToPrint)
    }

    /**
     * writeToConsoleLn method. Print text using println.
     *
     * @param whatToPrint String?
     */
    fun writeToConsoleLn(whatToPrint: String?){
        println(whatToPrint)
    }

}